"""
/main-srv/src/services/datetime_context.py

Модуль формирования темпорального контекста для промптов LLM-агента.

Логика работы:
Генерирует стандартизированный текстовый блок с текущими датой, временем и днем недели.
Блок оборачивается в теги <Текущее время> ... </Текущее время> и дополняется 
целевой подсказкой (hint), которая зависит от параметра `purpose`:

Цели (purpose):
- 'merge'     → Подсказка для оценки свежести фактов (новые заменяют устаревшие).
- 'summarize' → Подсказка для учета актуальности при построении summary.
- 'response'  → Подсказка для понимания относительных дат (вчера, завтра, на неделе).
- 'general'   → Базовая подсказка для общих темпоральных рассуждений (по умолчанию).

Использование:
    time_block = build_time_block('response')
    # Инъекция в системный или пользовательский промпт перед вызовом LLM.
"""

__version__ = "1.1.0"
__description__ = "Module for adding temporary context to agent requests"


from datetime import datetime

DAYS_RU = ["Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье"]

def build_time_block(purpose: str = "general") -> str:
    now = datetime.now()
    hint = {
        "merge": "Используй для оценки свежести: новые факты заменяют устаревшие.",
        "summarize": "Учитывай актуальность при построении summary.",
        "response": "Используй для понимания относительных дат (вчера, сейчас, завтра, на неделе).",
        "general": "Используй для темпоральных рассуждений.",
    }.get(purpose, "")
    
    return (
        f"\n\n<Текущее время>\n"
        f"Сейчас: {now.strftime('%Y-%m-%d %H:%M')} ({DAYS_RU[now.weekday()]})\n"
        f"{hint}\n"
        f"</Текущее время>\n"
    )